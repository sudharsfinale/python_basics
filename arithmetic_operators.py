# Arithmetic operations
print(10 + 3)  # addition Output => 13
print(10 - 3)  # subtraction Output => 7
print(10 * 3)  # multiplication Output => 30
print(10 / 3)  # division Output => 3.333333
print(71 // 12)  # floor division Output => 5
print(10 % 3)  # modulus Output => 1
print(10 ** 3)  # exponentiation Output => 1000
